package bean;
public class LecturerBean {

    private int lecturer_id;
    private String lecturer_firstname;
    private String lecturer_lastname;
    private String lecturer_mobilenumber;
    private String lecturer_address;
    private String lecturer_username;
    private String lecturer_password;
    public int getLecturer_id() {
        return lecturer_id;
    }
    public void setLecturer_id(int lecturer_id) {
        this.lecturer_id = lecturer_id;
    }
    public String getLecturer_firstname() {
        return lecturer_firstname;
    }
    public void setLecturer_firstname(String lecturer_firstname) {
        this.lecturer_firstname = lecturer_firstname;
    }
    public String getLecturer_lastname() {
        return lecturer_lastname;
    }
    public void setLecturer_lastname(String lecturer_lastname) {
        this.lecturer_lastname = lecturer_lastname;
    }
    public String getLecturer_mobilenumber() {
        return lecturer_mobilenumber;
    }
    public void setLecturer_mobilenumber(String lecturer_mobilenumber) {
        this.lecturer_mobilenumber = lecturer_mobilenumber;
    }
    public String getLecturer_address() {
        return lecturer_address;
    }
    public void setLecturer_address(String lecturer_address) {
        this.lecturer_address = lecturer_address;
    }
    public String getLecturer_username() {
        return lecturer_username;
    }
    public void setLecturer_username(String lecturer_username) {
        this.lecturer_username = lecturer_username;
    }
    public String getLecturer_password() {
        return lecturer_password;
    }
    public void setLecturer_password(String lecturer_password) {
        this.lecturer_password = lecturer_password;
    }




}
